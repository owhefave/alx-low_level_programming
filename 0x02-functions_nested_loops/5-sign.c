#include "main.h"
/**
 * print_sign - to print number signs
 * @n: character whose symbol is to be checked
 * Return: to return expected value
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
	_putchar('\n');
}

